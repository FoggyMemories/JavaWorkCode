public class task2 {
    public static void main(String[] args) {
        /*
        输出3*2的“￥"组成的实心矩形
         */
        for (int i = 1; i <= 3; i++) {
            for (int j = 1; j <= 2; j++) {
                System.out.print("￥\t");
            }
            System.out.println();
        }
    }
}
